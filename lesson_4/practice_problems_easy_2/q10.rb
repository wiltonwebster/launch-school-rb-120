# What are the benefits of using Object Oriented Programming in Ruby? Think of 
# as many as you can.

=begin
1. Encapsulation - parts of the program can be compartimentalized and hidden so
that code is easier to maintain. 
2. Polymorphism - Different types of objects can respond to the same method 
calls (though potentially in different ways). This allows for more flexibility
and prevents developers from "running out" of names.
3. Namespacing - Distinct hierarchies can be separated into modules with 
distinct names so that items within the hierarchies that have similar names can
easily still be distinguished.
4. Objects represent real world items. OOP is closer to real-world descriptions
of problems and allows developers to create more abstract systems.
5. Code broken down into discrete classes and modules is easier to change and
reuse (see Encapsulation).